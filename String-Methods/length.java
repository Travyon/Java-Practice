/**
 * The length method gives returns the length
 * or total number of characters, of the string 
 * it's called on.
 * 
 * Expect spaces and punctuation
 * to be included in the count.
 */

public class length {
    public static void main(String[] args){
        String str1 = "Travyon Stevens";
        String str2 = "Takari Coulter";

        System.out.println(str1.length());
        System.out.println(str2.length());

    }
    
}
