/**
 * To call a method:
 * 
 * Type the name of the method followed by the 
 * parentheses '()' and a semicolon ';'
 */

public class CallingaMethod {

    public static void exampleMethod(){
        System.out.println("Hello Method!");
    }

    public static void main(String[] args){
        exampleMethod();
        exampleMethod();
        exampleMethod();

    }
    
}

   
    

