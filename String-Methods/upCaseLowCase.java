/**
 * '.toUpperCase()' returns a string value
 * converted to uppercase.
 * 
 * '.toLowerCase()' returns a string value 
 * being converted to lowercase.
 * 
 */


public class upCaseLowCase {
    public static void main(String[] args){

        String randomWord = "Discipline";
        
        System.out.println(randomWord.toUpperCase());
        System.out.println(randomWord.toLowerCase());

    }
    
}
