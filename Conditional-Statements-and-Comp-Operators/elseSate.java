/*
* Executes a block of code when the condition inside
* if statement is false. 
* Else statement is always the last cod
*
*/


public class elseSate {
    public static void main(String[] args){
        int num1 = 5;
        int num2 = 6;

        if(num1 > num2){
            System.out.println("Big number");
        }
        else{
            System.out.println("Small number");
        }
    }

    
}
