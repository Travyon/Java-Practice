/**
 * '.charAt()' returns the character located 
 * at the string's specified index
 */

public class charAt {
    public static void main(String[] args){

        String money = "dollar";
        /**
         * Prints: 'l' because 
         * I argue index 2 in 
         * the method.
         */
        System.out.println(money.charAt(2));
    }
    
}
