/**
 * Made up of three parts separated by a semicolon:
 * 1. Intitialization of the loop control variable.
 * 2. A boolean expression.
 * 3. An increment or decrement statement.
 *  inclusive - all numbers
 * 
 * Step 1: checks to see if number is less than 
 * or equal to 100.
 * Step 2: if number is less than or equal to 100, the statement runs
 * Step 3: number increments by 1
 * Step 4: repeat steps 1 - 3
*/



public class forloop {
    public static void main(String[] args){
        //Prints 0 to 100
        for(int i = 0; i <= 100; i++){
            System.out.println(i);
        }
    }
    
}
